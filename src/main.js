import { fetchImages } from './js/pixabay-api.js';
import {
  renderImages,
  clearGallery,
  showLoadMoreButton,
  hideLoadMoreButton,
  showEndMessage,
  hideEndMessage,
  showLoader,
  hideLoader
} from './js/render-functions.js';
import iziToast from 'izitoast';
import 'izitoast/dist/css/iziToast.min.css';

const form = document.querySelector('.search-form');
const loadMoreButton = document.querySelector('.load-more');

let searchQuery = '';
let currentPage = 1;
const perPage = 40;
let totalHits = 0;

// **ðŸ”¹ Ð¡ÐºÑ€Ñ‹Ð²Ð°ÐµÐ¼ ÐºÐ½Ð¾Ð¿ÐºÑƒ Ð¸ ÑÐ¾Ð¾Ð±Ñ‰ÐµÐ½Ð¸Ðµ Ð¿Ñ€Ð¸ Ð·Ð°Ð³Ñ€ÑƒÐ·ÐºÐµ ÑÑ‚Ñ€Ð°Ð½Ð¸Ñ†Ñ‹**
hideLoadMoreButton();
hideEndMessage();

// **ðŸ”¹ ÐžÐ±Ñ€Ð°Ð±Ð¾Ñ‚Ñ‡Ð¸Ðº Ð¾Ñ‚Ð¿Ñ€Ð°Ð²ÐºÐ¸ Ñ„Ð¾Ñ€Ð¼Ñ‹**
form.addEventListener('submit', async event => {
  event.preventDefault();

  searchQuery = event.target.elements.searchQuery.value.trim();
  if (!searchQuery) {
    iziToast.warning({ title: 'Warning', message: 'Please enter a search term!', position: 'topRight' });
    return;
  }

  currentPage = 1;
  clearGallery();
  hideLoadMoreButton();
  hideEndMessage();
  showLoader(); // **ÐŸÐ¾ÐºÐ°Ð·Ñ‹Ð²Ð°ÐµÐ¼ Ð»Ð¾Ð°Ð´ÐµÑ€ Ð¿Ñ€Ð¸ Ð½Ð°Ñ‡Ð°Ð»Ðµ Ð·Ð°Ð¿Ñ€Ð¾ÑÐ°**

  try {
    const response = await fetchImages(searchQuery, currentPage, perPage);
    hideLoader(); // **Ð¡ÐºÑ€Ñ‹Ð²Ð°ÐµÐ¼ Ð»Ð¾Ð°Ð´ÐµÑ€ Ð¿Ð¾ÑÐ»Ðµ Ð¿Ð¾Ð»ÑƒÑ‡ÐµÐ½Ð¸Ñ Ð´Ð°Ð½Ð½Ñ‹Ñ…**

    if (!response || !response.hits.length) {
      iziToast.info({ title: 'Info', message: 'No images found for your query.', position: 'topRight' });
      return;
    }

    totalHits = Math.min(response.totalHits, 500);
    renderImages(response.hits);

    if (totalHits > perPage) {
      showLoadMoreButton();
    }
  } catch (error) {
    hideLoader();
    console.error('Error fetching images:', error);
  }
});

// **ðŸ”¹ ÐžÐ±Ñ€Ð°Ð±Ð¾Ñ‚Ñ‡Ð¸Ðº ÐºÐ»Ð¸ÐºÐ° Ð½Ð° ÐºÐ½Ð¾Ð¿ÐºÑƒ "Load More"**
loadMoreButton.addEventListener('click', async () => {
  if (document.querySelectorAll('.gallery-item').length >= totalHits) {
    hideLoadMoreButton();
    showEndMessage();
    return;
  }

  currentPage += 1;
  showLoader(); // **ÐŸÐ¾ÐºÐ°Ð·Ñ‹Ð²Ð°ÐµÐ¼ Ð»Ð¾Ð°Ð´ÐµÑ€ Ð¿Ñ€Ð¸ Ð·Ð°Ð³Ñ€ÑƒÐ·ÐºÐµ Ð½Ð¾Ð²Ñ‹Ñ… Ð¸Ð·Ð¾Ð±Ñ€Ð°Ð¶ÐµÐ½Ð¸Ð¹**

  try {
    const response = await fetchImages(searchQuery, currentPage, perPage);
    hideLoader(); // **Ð¡ÐºÑ€Ñ‹Ð²Ð°ÐµÐ¼ Ð»Ð¾Ð°Ð´ÐµÑ€ Ð¿Ð¾ÑÐ»Ðµ Ð¾Ñ‚Ð²ÐµÑ‚Ð°**

    if (response && response.hits.length > 0) {
      renderImages(response.hits, true);
    }

    if (document.querySelectorAll('.gallery-item').length >= totalHits) {
      hideLoadMoreButton();
      showEndMessage();
    }
  } catch (error) {
    hideLoader();
    console.error('Error loading more images:', error);
  }
});





